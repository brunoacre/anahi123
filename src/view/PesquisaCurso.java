    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        //descobrir a linha selecionada na jTable
        int linha = tblResultado.getSelectedRow();
        //verificar se usuário selecionou linha
        if(linha == -1){
            JOptionPane.showMessageDialog
                        (this, "Selecione um curso para exluir!");
        } else {
            //recupera o curso selecionado
            Curso aux = listaCursos.get(linha);
            //cria o DAO de curso
            CursoDAO dao = new CursoDAO();
            //chamar o método de DELETE
            if(dao.excluir(aux.getIdCurso())){
                DefaultTableModel modelo = 
                   (DefaultTableModel) tblResultado.getModel();
                modelo.removeRow(linha);
                
                JOptionPane.showMessageDialog(this, 
                            "Excluído com Sucesso!");
            }else{
                 JOptionPane.showMessageDialog(this, 
                            "Falha ao excluir!");
            }
        }       
        
        
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        int linha = tblResultado.getSelectedRow();
        if(linha == -1){
            JOptionPane.showMessageDialog(this, 
                    "Selecione uma linha!");
        }else{
            Curso c = listaCursos.get(linha);
            TelaCurso tela = new TelaCurso();
            //chamar o método que recebe objeto a 
            //ser alterado
            tela.carregaAlteracao(c);
            tela.setVisible(true);
            //esconder a tela de pesquisa
            this.dispose();
        }
        
        
    }//GEN-LAST:event_btnAlterarActionPerformed

