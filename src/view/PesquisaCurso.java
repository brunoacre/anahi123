    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        //recuperar os cursos do BD, usando o DAO
        CursoDAO dao = new CursoDAO();
        //limpar a lista
        listaCursos.clear();
        //carregar os cursos do BD via DAO
        listaCursos = dao.recuperaTodos();
        //exibir os cursos na tabela
        //recuperar o modelo da tabela
        DefaultTableModel modelo = 
            (DefaultTableModel)tblResultado.getModel();
        //limpar a tabela
        modelo.setNumRows(0);
        //percorrer a lista de cursos com foreach
        for(Curso c : listaCursos){
            //adicionar a linha com o conteúdo das colunas
            modelo.addRow(new String[]{c.getNomeCurso(),
                                c.getDescricao()});
        }
        //definir o modelo na tabela
        tblResultado.setModel(modelo);
        
        
    }//GEN-LAST:event_formWindowOpened

